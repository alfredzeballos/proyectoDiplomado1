    private void jComboBoxDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxDatosActionPerformed
        // TODO add your handling code here:        
        if(tablaSeleccionada!=-1 && atributoSeleccionado!=-1){                
                if(!jAtributos.getValueAt(atributoSeleccionado,5).equals("------") && jAtributos.getValueAt(atributoSeleccionado,1).equals("varchar") && Integer.parseInt(jAtributos.getValueAt(atributoSeleccionado,2).toString())>10){
                    jAtributos.setValueAt(jComboBoxDatos.getSelectedItem(),atributoSeleccionado,5);                
                    poblar.getListaTablas().get(tablaSeleccionada).getlAtributos().getListaAtributos().get(atributoSeleccionado).setElegirDatoInsertar(generar.devolverElegirDatoInsertar((String) jComboBoxDatos.getSelectedItem()));
                    copiaTablas=poblar.getListaTablas();
                }                
        }        
    }//GEN-LAST:event_jComboBoxDatosActionPerformed

